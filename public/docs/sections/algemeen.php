        <h5>Docenten</h5>
        <p>Zetten een aantal datums en tijden open in het systeem. Ook kunnen zij het aantal examinerende docenten aangeven zodat het systeem weet hoeveel keer deze datum en tijd openstaat en kunnen zij aangeven of het een eerste kan of een herkansing is. Er kunnen meerdere examen tegelijkertijd open staan. Er wordt in het systeem aangegeven van wanneer tot wanneer een student zich kan aanmelden en eventueel een uitklappende uitleg voor het examen. De docent kan d.m.v. een excel bestand een planning zien van alle opgegeven studenten. De docent kan een overzicht zien van welke studenten per klas zich hebben aangemeld. Als wij hiervoor eerst alle klassen eenmalig in het systeem moeten zetten is dit geen probleem. </p>
        <h5>Studenten</h5>
        <p>Kunnen zich alleen opgeven met hun eigen account en voor hen zelf. Studenten loggen in en kiezen het vak/examen uit waarvoor zij zich willen opgeven. Zij kiezen een datum en tijd. Het mag zichtbaar zijn welke tijdsvlakken of datums al bezet zijn. Ze geven aan of ze recht hebben op extra faciliteiten en voeren hun pasnummer of de type faciliteiten. Ook is er een ruimte voor bijzonderheden/notities.  De examenregelementen staan in een pop-out in het overzicht en ze geven akkoord op alle ingevoerde zaken. De student ontvangt een bevestigingsmail van het opgegeven examen.</p>
